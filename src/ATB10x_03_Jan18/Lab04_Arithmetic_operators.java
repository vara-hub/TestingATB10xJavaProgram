package ATB10x_03_Jan18;

public class Lab04_Arithmetic_operators {
    public static void main(String[] args) {
        // Arithmetic Operators
        //        + (Addition)
        //        - (Subtraction)
        //       * (Multiplication)
        //       /  (Division)
         //      % (Modulo) | Modulus - Remainder
        int a = 10;
        int b = 6;
        float c = 3f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/c);
        System.out.println(a%c);
    }
}
