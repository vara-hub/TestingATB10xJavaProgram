package ATB10x_03_Jan18;

public class Lab05_Assignment_Operators {
    public static void main(String[] args) {

        // Assignment Operators
        //      +=, -=, /=, *=
        int age = 10;
        age +=10;  // +=
        System.out.println(age);

        int age1 = 10;
        age1 -=10;   // -=
        System.out.println(age1);

        int age2 = 10;
        age2 *=10;  // *=
        System.out.println(age2);

        int age3 = 10;
        age3 /=10;  //   /=
        System.out.println(age3);

        int age4 = 10;
        age4 %=10;
        System.out.println(age4);


        // Assignment operator

        int sar10 = 90;

        // int -> data type
        // age -> identifier
        // 90 -> Literal
        // = -> ? operator
        System.out.println(sar10);
        // RHV to the LH
    }
}
